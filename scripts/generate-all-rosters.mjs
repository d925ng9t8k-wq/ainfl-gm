/**
 * Regenerate allRosters.js from all-teams-raw.json using ALL players (not top 15).
 * Run: node ~/Projects/BengalOracle/scripts/generate-all-rosters.mjs
 */
import { readFileSync, writeFileSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const RAW_JSON = join(__dirname, 'all-teams-raw.json');
const OUT_JS = join(__dirname, '..', 'src', 'data', 'allRosters.js');

const SLUG_TO_ABBR = {
  'arizona-cardinals': 'ARI', 'atlanta-falcons': 'ATL', 'baltimore-ravens': 'BAL',
  'buffalo-bills': 'BUF', 'carolina-panthers': 'CAR', 'chicago-bears': 'CHI',
  'cincinnati-bengals': 'CIN', 'cleveland-browns': 'CLE', 'dallas-cowboys': 'DAL',
  'denver-broncos': 'DEN', 'detroit-lions': 'DET', 'green-bay-packers': 'GB',
  'houston-texans': 'HOU', 'indianapolis-colts': 'IND', 'jacksonville-jaguars': 'JAX',
  'kansas-city-chiefs': 'KC', 'las-vegas-raiders': 'LV', 'los-angeles-chargers': 'LAC',
  'los-angeles-rams': 'LAR', 'miami-dolphins': 'MIA', 'minnesota-vikings': 'MIN',
  'new-england-patriots': 'NE', 'new-orleans-saints': 'NO', 'new-york-giants': 'NYG',
  'new-york-jets': 'NYJ', 'philadelphia-eagles': 'PHI', 'pittsburgh-steelers': 'PIT',
  'san-francisco-49ers': 'SF', 'seattle-seahawks': 'SEA', 'tampa-bay-buccaneers': 'TB',
  'tennessee-titans': 'TEN', 'washington-commanders': 'WSH',
};

const allResults = JSON.parse(readFileSync(RAW_JSON, 'utf-8'));
const rosters = {};
let grandTotal = 0;

for (const [slug, data] of Object.entries(allResults)) {
  const abbr = SLUG_TO_ABBR[slug];
  if (!abbr) continue;

  // Skip CIN - already in bengalsRoster.js
  if (abbr === 'CIN') {
    console.log(`  ${abbr}: Skipped (use bengalsRoster.js)`);
    continue;
  }

  // ALL players sorted by cap hit descending
  const allPlayers = (data.players || [])
    .sort((a, b) => b.capHit - a.capHit)
    .map((p, idx) => ({
      id: idx + 1,
      name: p.name,
      position: 'UNK',
      age: 27,
      capHit: p.capHit,
      deadMoney: 0,
      capSavings: p.capHit,
      baseSalary: p.capHit,
      contractYears: 4,
      contractTotal: Math.round(p.capHit * 4 * 100) / 100,
      yearsRemaining: 2,
      isFranchise: false,
    }));

  const totalCapUsed = (data.players || []).reduce((sum, p) => sum + p.capHit, 0);

  rosters[abbr] = {
    players: allPlayers,
    capSummary: {
      totalCap: data.totalCap || 301.2,
      capUsed: Math.round(totalCapUsed * 100) / 100,
      deadCap: 0,
      capSpace: data.capSpace || 0,
    },
  };

  grandTotal += allPlayers.length;
  console.log(`  ${abbr}: ${allPlayers.length} players, cap space: ${data.capSpace || 0}M`);
}

// Build the JS file
const js = `// Auto-generated by generate-all-rosters.mjs on ${new Date().toISOString()}
// Source: Over The Cap (overthecap.com) 2026 salary cap data
// ALL players for each team (except CIN which uses bengalsRoster.js)
// Position set to 'UNK' and age to 27 as defaults — cap hits are the critical data
export const allRosters = ${JSON.stringify(rosters, null, 2)};
`;

writeFileSync(OUT_JS, js);
console.log(`\nGenerated ${OUT_JS}`);
console.log(`  Teams: ${Object.keys(rosters).length}`);
console.log(`  Total players: ${grandTotal}`);
