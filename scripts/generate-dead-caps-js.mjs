import { readFileSync, writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const data = JSON.parse(readFileSync(join(__dirname, 'dead-caps.json'), 'utf-8'));
const OUT = join(__dirname, '..', 'src', 'data', 'teamDeadCaps.js');

let js = `// Auto-generated by scrape-dead-caps.mjs on ${new Date().toISOString()}
// Source: Over The Cap (overthecap.com) 2026 dead money charges
// All 32 NFL teams
export const teamDeadCaps = {
`;

const abbrs = Object.keys(data).sort();
for (const abbr of abbrs) {
  const entries = data[abbr];
  js += `  ${abbr}: [\n`;
  for (const e of entries) {
    const millions = Math.round((e.amount / 1000000) * 100) / 100;
    const name = e.name.replace(/'/g, "\\'");
    js += `    { name: '${name}', reason: 'Departed', amount: ${millions} },\n`;
  }
  js += `  ],\n`;
}

js += `};\n`;

writeFileSync(OUT, js);
console.log(`Generated ${OUT}`);
