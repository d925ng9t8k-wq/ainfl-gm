#!/usr/bin/env node
/**
 * fix-years-remaining.mjs
 *
 * The 2026 season hasn't been played yet, so 2026 counts as a remaining year.
 * OTC data excluded the current year — this script increments yearsRemaining by 1
 * for every player with capHit > 0 in both allRosters.js and bengalsRoster.js.
 *
 * Also applies specific known-contract overrides for select players.
 */

import { readFileSync, writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const dataDir = resolve(__dirname, '..', 'src', 'data');

// ─── 1. Fix allRosters.js ───────────────────────────────────────────────────

const allRostersPath = resolve(dataDir, 'allRosters.js');
let allRostersRaw = readFileSync(allRostersPath, 'utf-8');

// Strip everything before the opening { of the object literal
const exportIdx = allRostersRaw.indexOf('export const allRosters');
const braceIdx = allRostersRaw.indexOf('{', exportIdx);
const allRostersJsonStr = allRostersRaw
  .slice(braceIdx)
  .replace(/;\s*$/, '');

const allRosters = JSON.parse(allRostersJsonStr);

let totalAllRostersAffected = 0;

for (const teamAbbr of Object.keys(allRosters)) {
  const team = allRosters[teamAbbr];
  for (const player of team.players) {
    if (player.capHit > 0) {
      player.yearsRemaining += 1;
      totalAllRostersAffected++;
    }
    // Ensure contractYears >= yearsRemaining
    if (player.contractYears < player.yearsRemaining) {
      player.contractYears = player.yearsRemaining;
    }
  }
}

// ─── Known contract overrides ───────────────────────────────────────────────

const overrides = [
  { team: 'LV', name: 'Maxx Crosby',       yearsRemaining: 4, contractYears: 4 },
  { team: 'LV', name: 'Tyler Linderbaum',  yearsRemaining: 3, contractYears: 3 },
  { team: 'BUF', name: 'Josh Allen',       yearsRemaining: 3, contractYears: 4 },
  { team: 'KC',  name: 'Patrick Mahomes',  yearsRemaining: 6, contractYears: 6 },
  { team: 'BAL', name: 'Lamar Jackson',    yearsRemaining: 2, contractYears: 4 },
];

for (const ov of overrides) {
  const team = allRosters[ov.team];
  if (!team) { console.warn(`Team ${ov.team} not found`); continue; }
  const player = team.players.find(p => p.name === ov.name);
  if (!player) { console.warn(`Player ${ov.name} not found on ${ov.team}`); continue; }
  const oldYr = player.yearsRemaining;
  player.yearsRemaining = ov.yearsRemaining;
  player.contractYears = ov.contractYears;
  console.log(`  Override: ${ov.name} (${ov.team}) yearsRemaining ${oldYr} → ${ov.yearsRemaining}, contractYears → ${ov.contractYears}`);
}

// Write back
const header = `// Auto-generated by update-all-rosters.mjs on 2026-03-18T04:55:07.635Z
// Source: Over The Cap (overthecap.com) 2026 salary cap data + ESPN roster data
// ALL players for each team (except CIN which uses bengalsRoster.js)
// Financial data from OTC, positions/ages from ESPN
// yearsRemaining fix applied: +1 to include 2026 season (fix-years-remaining.mjs)
export const allRosters = `;

writeFileSync(allRostersPath, header + JSON.stringify(allRosters, null, 2) + ';\n', 'utf-8');
console.log(`\nallRosters.js: ${totalAllRostersAffected} players updated (+1 yearsRemaining)`);

// ─── 2. Fix bengalsRoster.js ────────────────────────────────────────────────

const bengalsPath = resolve(dataDir, 'bengalsRoster.js');
let bengalsRaw = readFileSync(bengalsPath, 'utf-8');

let bengalsAffected = 0;

// Use regex to increment yearsRemaining for every player with capHit > 0
// Match each player object and update yearsRemaining
bengalsRaw = bengalsRaw.replace(
  /capHit:\s*([\d.]+),([^}]*?)yearsRemaining:\s*(\d+)/g,
  (match, capHit, middle, yr) => {
    const cap = parseFloat(capHit);
    const oldYr = parseInt(yr, 10);
    if (cap > 0) {
      bengalsAffected++;
      const newYr = oldYr + 1;
      return `capHit: ${capHit},${middle}yearsRemaining: ${newYr}`;
    }
    return match;
  }
);

// Also fix contractYears where needed — ensure contractYears >= yearsRemaining
// Parse each player line and check
bengalsRaw = bengalsRaw.replace(
  /contractYears:\s*(\d+),([^}]*?)yearsRemaining:\s*(\d+)/g,
  (match, cy, middle, yr) => {
    const contractYears = parseInt(cy, 10);
    const yearsRemaining = parseInt(yr, 10);
    if (contractYears < yearsRemaining) {
      return `contractYears: ${yearsRemaining},${middle}yearsRemaining: ${yr}`;
    }
    return match;
  }
);

writeFileSync(bengalsPath, bengalsRaw, 'utf-8');
console.log(`bengalsRoster.js: ${bengalsAffected} players updated (+1 yearsRemaining)`);

// ─── 3. Verification ───────────────────────────────────────────────────────

console.log('\n--- Verification ---');

// Re-read allRosters to verify
const verifyAllStr = readFileSync(allRostersPath, 'utf-8')
  .replace(/^[\s\S]*?export\s+const\s+allRosters\s*=\s*/, '')
  .replace(/;\s*$/, '');
const verifyAll = JSON.parse(verifyAllStr);

const checks = [
  { team: 'LV', name: 'Maxx Crosby', expected: 4 },
  { team: 'LV', name: 'Tyler Linderbaum', expected: 3 },
  { team: 'BUF', name: 'Josh Allen', expected: 3 },
  { team: 'KC', name: 'Patrick Mahomes', expected: 6 },
  { team: 'BAL', name: 'Lamar Jackson', expected: 2 },
];

for (const c of checks) {
  const p = verifyAll[c.team].players.find(p => p.name === c.name);
  const status = p.yearsRemaining === c.expected ? 'OK' : 'MISMATCH';
  console.log(`  ${c.name} (${c.team}): yearsRemaining=${p.yearsRemaining} (expected ${c.expected}) [${status}]`);
}

// Verify Bengals from file
const bengalsVerify = readFileSync(bengalsPath, 'utf-8');
const bengalsChecks = [
  { name: 'Joe Burrow', expected: 4 },
  { name: "Ja'Marr Chase", expected: 4 },
  { name: 'Tee Higgins', expected: 4 },
];
for (const c of bengalsChecks) {
  const match = bengalsVerify.match(new RegExp(`name:\\s*'${c.name.replace(/'/g, "\\'")}',.*?yearsRemaining:\\s*(\\d+)`, 's'));
  if (!match) {
    // Try double quotes
    const match2 = bengalsVerify.match(new RegExp(`name:\\s*"${c.name}",.*?yearsRemaining:\\s*(\\d+)`, 's'));
    if (match2) {
      const status = parseInt(match2[1]) === c.expected ? 'OK' : 'MISMATCH';
      console.log(`  ${c.name} (CIN): yearsRemaining=${match2[1]} (expected ${c.expected}) [${status}]`);
    } else {
      console.log(`  ${c.name} (CIN): NOT FOUND`);
    }
  } else {
    const status = parseInt(match[1]) === c.expected ? 'OK' : 'MISMATCH';
    console.log(`  ${c.name} (CIN): yearsRemaining=${match[1]} (expected ${c.expected}) [${status}]`);
  }
}

console.log(`\nTotal players affected: ${totalAllRostersAffected + bengalsAffected}`);
